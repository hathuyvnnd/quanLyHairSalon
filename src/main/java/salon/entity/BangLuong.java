public class BangLuong {
    int thang, nam;

    public BangLuong(int thang, int nam) {
        this.thang = thang;
        this.nam = nam;
    }

    public BangLuong() {
    }

    public int getThang() {
        return thang;
    }

    public void setThang(int thang) {
        this.thang = thang;
    }

    public int getNam() {
        return nam;
    }

    public void setNam(int nam) {
        this.nam = nam;
    }
    
}
